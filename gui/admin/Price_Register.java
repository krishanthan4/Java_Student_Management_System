    private void TableLoad() {
        try {

            ResultSet resultset = MySQL.execute("SELECT * FROM price");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

            while (resultset.next()) {
                Vector vector = new Vector();
                String price = resultset.getString(String.valueOf("Price"));
                vector.add(price);

                model.addRow(vector);

//        System.out.println(resultset.getString(String.valueOf("Price")));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

